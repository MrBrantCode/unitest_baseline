from functools import lru_cache
from collections import deque

M = 10 ** 9 + 7

@lru_cache(None)
def inv(x):
    return pow(x, M - 2, M)

@lru_cache(None)
def dp(u, v):
    if u == 0:
        return 0
    if v == 0:
        return 1
    return (dp(u - 1, v) * inv(2) + dp(u, v - 1) * inv(2)) % M

def calc(n, link, start):
    res = 0
    for u in range(1, n + 1):
        for v in range(u + 1, n + 1):
            lca = (link[start][u] + link[start][v] - link[u][v]) // 2
            res += dp(link[start][u] - lca, link[start][v] - lca)
            res %= M
    return res

def calculate_expected_inversions(n, edges):
    graph = [[] for _ in range(n + 1)]
    for u, v in edges:
        graph[u].append(v)
        graph[v].append(u)
    
    link = [[-1 for _ in range(n + 1)] for _ in range(n + 1)]
    for i in range(1, n + 1):
        link[i][i] = 0
        q = deque()
        q.append(i)
        while q:
            u = q.popleft()
            for v in graph[u]:
                if link[i][v] >= 0:
                    continue
                link[i][v] = link[i][u] + 1
                q.append(v)
    
    ans = 0
    for root in range(1, n + 1):
        ans += calc(n, link, start=root)
        ans %= M
    
    ans *= inv(n)
    ans %= M
    
    return ans