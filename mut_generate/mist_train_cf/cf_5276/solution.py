"""
QUESTION:
Implement the `merge_sort` function to sort a large array of tuples in ascending order based on the first element of the tuple while maintaining the stability of the sort (i.e., preserving the relative order of equal elements based on the second element of the tuple). The function should have a time complexity of O(n log n) and use a constant amount of additional memory.
"""

def merge_sort(arr):
    if len(arr) <= 1:
        return arr

    mid = len(arr) // 2
    left_half = arr[:mid]
    right_half = arr[mid:]

    left_half = merge_sort(left_half)
    right_half = merge_sort(right_half)

    return merge(left_half, right_half)

def merge(left, right):
    result = []
    i = 0
    j = 0

    while i < len(left) and j < len(right):
        if left[i] <= right[j]:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1

    result.extend(left[i:])
    result.extend(right[j:])
    return result