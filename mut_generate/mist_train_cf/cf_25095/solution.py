"""
QUESTION:
Write a function named 'multiply' to calculate the result of multiplying two given numbers. The function should take two integers as input and return their product.
"""

def multiply(a, b):
    """
    Calculate the product of two integers.

    Args:
        a (int): The first number.
        b (int): The second number.

    Returns:
        int: The product of a and b.
    """
    return a * b