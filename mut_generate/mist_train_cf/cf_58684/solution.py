"""
QUESTION:
Create a function called sort_string_array that takes an array of strings as input and returns the sorted array in alphabetical order. The input array will contain only string elements and will not be empty. The function should be case-sensitive.
"""

def sort_string_array(string_array):
    """
    Sorts an array of strings in alphabetical order.

    Args:
        string_array (list): The input array of strings.

    Returns:
        list: The sorted array of strings.
    """
    return sorted(string_array)