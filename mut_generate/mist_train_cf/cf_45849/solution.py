"""
QUESTION:
Write a function called `reverse_string` that takes a string as input and returns the reversed string without using any looping mechanisms.
"""

def reverse_string(input_string):
    """
    Reverses a given string without using any looping mechanisms.
    
    Args:
        input_string (str): The string to be reversed.
    
    Returns:
        str: The reversed string.
    """
    return input_string[::-1]