"""
QUESTION:
Write a function `sixth_position_element` to find the element at the sixth position in a given numerical sequence. The sequence is 0-indexed, meaning the first element is at index 0.
"""

def sixth_position_element(num_sequence):
    """
    This function finds the element at the sixth position in a given numerical sequence.
    
    Parameters:
    num_sequence (list): A list of numbers.
    
    Returns:
    int: The element at the sixth position in the sequence.
    """
    return num_sequence[5]