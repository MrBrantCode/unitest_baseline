"""
QUESTION:
Write a function named `merge_sort` that sorts a given list of numbers in descending order using the Merge Sort algorithm with a time complexity of O(nlogn). The function should take a list of numbers as input and return the sorted list. The Merge Sort algorithm should divide the input list into two halves, recursively sort each half, and then merge the two sorted halves back together into a single sorted list in descending order.
"""

def merge_sort(arr):
    if len(arr) <= 1:
        return arr
    
    mid = len(arr) // 2
    left = arr[:mid]
    right = arr[mid:]
    
    left = merge_sort(left)
    right = merge_sort(right)
    
    return merge(left, right)

def merge(left, right):
    merged = []
    i = j = 0
    
    while i < len(left) and j < len(right):
        if left[i] > right[j]:
            merged.append(left[i])
            i += 1
        else:
            merged.append(right[j])
            j += 1
    
    while i < len(left):
        merged.append(left[i])
        i += 1
    
    while j < len(right):
        merged.append(right[j])
        j += 1
    
    return merged