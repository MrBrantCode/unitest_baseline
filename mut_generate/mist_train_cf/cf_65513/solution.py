"""
QUESTION:
Create a function `celsius_to_fahrenheit(celsius)` that takes a temperature in Celsius as input and returns its equivalent temperature in Fahrenheit. The conversion should be based on the formula `F = C * 9/5 + 32`, where `F` is the temperature in Fahrenheit and `C` is the temperature in Celsius. The function should be able to handle any valid Celsius temperature.
"""

def celsius_to_fahrenheit(celsius):
    return celsius * 9.0 / 5.0 + 32