"""
QUESTION:
Write a function named `sum_constituents` that computes the summation of integers in a given list. The function should take a list of integers as input and return the total sum.
"""

def sum_constituents(lst):
    """
    This function calculates the summation of integers in a given list.

    Parameters:
    lst (list): A list of integers.

    Returns:
    int: The total sum of integers in the list.
    """
    return sum(lst)