"""
QUESTION:
Write a function `find_max_number` to identify the maximum numerical value in a given list of integers. The function should return the largest integer value.
"""

def find_max_number(numbers):
    """
    Returns the largest integer value in a given list of integers.

    Args:
        numbers (list): A list of integers.

    Returns:
        int: The largest integer value.
    """
    return max(numbers)