"""
QUESTION:
Write a function named `get_unicode_value` that takes a single character as input and returns its corresponding Unicode value.
"""

def get_unicode_value(character):
    """
    Returns the Unicode value of a given character.
    
    Args:
        character (str): A single character.
    
    Returns:
        int: The Unicode value of the character.
    """
    return ord(character)