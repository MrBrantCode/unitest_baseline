"""
QUESTION:
Write a function `vector_sum` that takes a vector of numbers as input and returns their sum. The function should be able to handle vectors of different numerical data types such as int, float, and double.
"""

def vector_sum(nums):
    """
    This function calculates the sum of a list of numbers.
    
    Args:
        nums (list): A list of numbers.
    
    Returns:
        The sum of the numbers in the list.
    """
    return sum(nums)