"""
QUESTION:
Implement a function `merge_sort(arr)` that sorts the input array `arr` in ascending order. The function should have a time complexity of O(n log n), be stable, and able to handle a variety of data types (including integers, floating-point numbers, strings, and arrays of objects) and large input sizes efficiently.
"""

def merge_sort(arr):
    if len(arr) <= 1:
        return arr
    
    mid = len(arr) // 2
    left = merge_sort(arr[:mid])
    right = merge_sort(arr[mid:])
    
    return merge(left, right)

def merge(left, right):
    result = []
    i = j = 0
    
    while i < len(left) and j < len(right):
        if left[i] <= right[j]:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1
    
    while i < len(left):
        result.append(left[i])
        i += 1
    
    while j < len(right):
        result.append(right[j])
        j += 1
    
    return result