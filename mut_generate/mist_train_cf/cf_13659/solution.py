"""
QUESTION:
Implement the `quicksort` function that sorts an array of elements using the quicksort algorithm. The function should take an array as input and return a sorted array.
"""

def quicksort(arr):
    if len(arr) <= 1:
        return arr
    pivot = arr[len(arr) // 2]
    left = [x for x in arr if x < pivot]
    middle = [x for x in arr if x == pivot]
    right = [x for x in arr if x > pivot]
    return quicksort(left) + middle + quicksort(right)