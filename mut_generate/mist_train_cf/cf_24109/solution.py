"""
QUESTION:
Create a function called sum_list that takes a list of integers as input and returns the sum of all the items in the list. The function should not take any other arguments and should return an integer.
"""

def sum_list(lst):
    """
    Returns the sum of all items in a given list of integers.
    
    Args:
        lst (list): A list of integers.
    
    Returns:
        int: The sum of all items in the list.
    """
    return sum(lst)