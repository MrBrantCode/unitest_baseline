"""
QUESTION:
Implement a function named `merge_sort` that takes an array of integers as input, sorts the elements in ascending order using a self-implemented sorting algorithm with a time complexity of O(nlogn) or better, and returns the sorted array. The function must handle arrays with duplicate elements and not use any extra arrays or data structures during the sorting process.
"""

def merge_sort(arr):
    if len(arr) <= 1:
        return arr
    
    mid = len(arr) // 2
    left = arr[:mid]
    right = arr[mid:]
    
    left = merge_sort(left)
    right = merge_sort(right)
    
    return merge(left, right)

def merge(left, right):
    result = []
    i = j = 0
    
    while i < len(left) and j < len(right):
        if left[i] < right[j]:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1
    
    while i < len(left):
        result.append(left[i])
        i += 1
    
    while j < len(right):
        result.append(right[j])
        j += 1
    
    return result