"""
QUESTION:
Write a function named `calculate_revenue` that takes two parameters: `num_units` and `price_per_unit`, which can be either integers or floats. The function should return the revenue calculated as the product of `num_units` and `price_per_unit`.
"""

def calculate_revenue(num_units, price_per_unit):
    revenue = num_units * price_per_unit
    return revenue