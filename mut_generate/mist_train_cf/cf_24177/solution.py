"""
QUESTION:
Convert an integer to a Roman numeral. The function should be named `int_to_roman`. The input is a positive integer, and the output is a string representing the Roman numeral equivalent of the input. The function should support integers in the range 1 to 3999.
"""

def int_to_roman(num):
    val = [
        1000, 900, 500, 400,
        100, 90, 50, 40,
        10, 9, 5, 4,
        1
        ]
    syb = [
        "M", "CM", "D", "CD",
        "C", "XC", "L", "XL",
        "X", "IX", "V", "IV",
        "I"
        ]
    roman_num = ''
    i = 0
    while  num > 0:
        for _ in range(num // val[i]):
            roman_num += syb[i]
            num -= val[i]
        i += 1
    return roman_num