"""
QUESTION:
Write a function `add_large_numbers` that takes two large integers `a` and `b` (each with a maximum value of 10^18) as input and returns their sum.
"""

def add_large_numbers(a: int, b: int) -> int:
    """
    Adds two large integers.

    Args:
        a (int): The first large integer.
        b (int): The second large integer.

    Returns:
        int: The sum of a and b.
    """
    return a + b