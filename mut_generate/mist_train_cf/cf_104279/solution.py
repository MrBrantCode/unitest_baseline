"""
QUESTION:
Write a function `find_max_score` that takes a set of scores as input and returns the maximum score without using any loops or conditional statements.
"""

def find_max_score(scores):
    """
    This function takes a set of scores as input and returns the maximum score.

    Args:
        scores (set): A set of scores.

    Returns:
        int: The maximum score.
    """
    return max(scores)