"""
QUESTION:
Create a function `fahrenheit_to_celsius` that takes a temperature in Fahrenheit as input and returns the corresponding temperature in Celsius. The function should use the formula `Celsius = (Fahrenheit - 32) * 5/9` for the conversion.
"""

def fahrenheit_to_celsius(fahrenheit):
    """
    Convert a temperature in Fahrenheit to Celsius.

    Parameters:
    fahrenheit (float): The temperature in Fahrenheit.

    Returns:
    float: The temperature in Celsius.
    """
    return (fahrenheit - 32) * 5/9