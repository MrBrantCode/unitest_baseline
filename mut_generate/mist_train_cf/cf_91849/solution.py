"""
QUESTION:
Write a function called "add_up_numbers" that takes a list of integers as input and returns the sum of all the numbers in the list without using the "+" operator or any arithmetic operators.
"""

def add_up_numbers(numbers):
    """
    Returns the sum of all the numbers in the input list without using the "+" operator or any arithmetic operators.
    
    Args:
    numbers (list): A list of integers.
    
    Returns:
    int: The sum of all the numbers in the list.
    """
    return sum(numbers)