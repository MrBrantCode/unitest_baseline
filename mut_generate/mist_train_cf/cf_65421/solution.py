"""
QUESTION:
Create a function called `exponential_magnitude` that takes an integer `x` as input and returns the result of 2 raised to the power of `x`.
"""

def exponential_magnitude(x):
    """
    This function takes an integer x as input and returns the result of 2 raised to the power of x.
    
    Args:
        x (int): The exponent to which 2 is raised.
    
    Returns:
        int: The result of 2 raised to the power of x.
    """
    return 2 ** x