"""
QUESTION:
Write a function `convert_to_float` that takes an integer as input and returns its float representation.
"""

def convert_to_float(n):
    """
    This function takes an integer as input and returns its float representation.

    Args:
        n (int): The integer to be converted.

    Returns:
        float: The float representation of the input integer.
    """
    return float(n)