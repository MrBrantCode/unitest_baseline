"""
QUESTION:
Create a function `bitwise_and` that performs a bitwise AND operation on two integers `a` and `b`. The function should return the result of the bitwise AND operation.

Restrictions: The function should use the built-in bitwise AND operator (&) to perform the operation. The function should not take any additional arguments other than the two integers `a` and `b`.
"""

def bitwise_and(a, b):
    """
    Performs a bitwise AND operation on two integers.

    Args:
        a (int): The first integer.
        b (int): The second integer.

    Returns:
        int: The result of the bitwise AND operation.
    """
    return a & b