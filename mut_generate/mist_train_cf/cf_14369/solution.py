"""
QUESTION:
Write a function `compare_variables` that takes two variables of different data types as input and returns the Boolean value resulting from comparing them using the == operator. The function should be able to handle any data types.
"""

def compare_variables(var1, var2):
    """
    This function compares two variables of different data types and returns the Boolean value.

    Args:
        var1: The first variable to compare.
        var2: The second variable to compare.

    Returns:
        bool: The result of comparing var1 and var2 using the == operator.
    """
    return var1 == var2