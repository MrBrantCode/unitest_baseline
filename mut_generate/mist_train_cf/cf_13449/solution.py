"""
QUESTION:
Implement a function named merge_sort that takes an array of integers as input, sorts it in ascending order, and returns the sorted array. The function must achieve a time complexity of O(nlogn) without using any built-in sorting functions or libraries. The function should handle duplicate values and maintain the relative order of equal elements.
"""

def merge_sort(arr):
    if len(arr) <= 1:
        return arr
    
    mid = len(arr) // 2
    left = merge_sort(arr[:mid])
    right = merge_sort(arr[mid:])
    
    return merge(left, right)


def merge(left, right):
    merged = []
    i = j = 0
    
    while i < len(left) and j < len(right):
        if left[i] <= right[j]:
            merged.append(left[i])
            i += 1
        else:
            merged.append(right[j])
            j += 1
    
    while i < len(left):
        merged.append(left[i])
        i += 1
    
    while j < len(right):
        merged.append(right[j])
        j += 1
    
    return merged