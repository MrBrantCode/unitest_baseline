"""
QUESTION:
Implement a function `find_square_root` to calculate the square root of a given number. The function should take an integer as input and return its square root.
"""

import math

def find_square_root(n):
    """
    Calculate the square root of a given number.
    
    Parameters:
    n (int): The input number.
    
    Returns:
    float: The square root of the input number.
    """
    return math.sqrt(n)