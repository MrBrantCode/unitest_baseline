"""
QUESTION:
Write a function `ConvertCelsiusToFahrenheit(celsius)` that takes a temperature in Celsius as input and returns the equivalent temperature in Fahrenheit. The function should apply the standard formula for converting Celsius to Fahrenheit: F = (C * 9/5) + 32.
"""

def ConvertCelsiusToFahrenheit(celsius):
    return (celsius * 9/5) + 32