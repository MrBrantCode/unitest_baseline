"""
QUESTION:
Implement a function `merge_sort(arr)` that takes a list of integers as input and returns the sorted list in increasing order. The function should have a time complexity of O(nlogn), not use any built-in sorting functions or methods, and only use the input list as the data structure. The input list can contain duplicate integers and may have a length up to 1000.
"""

def merge_sort(arr):
    if len(arr) <= 1:
        return arr

    mid = len(arr) // 2
    left_half = arr[:mid]
    right_half = arr[mid:]

    left_half = merge_sort(left_half)
    right_half = merge_sort(right_half)

    return merge(left_half, right_half)


def merge(left, right):
    merged = []
    i = 0
    j = 0

    while i < len(left) and j < len(right):
        if left[i] <= right[j]:
            merged.append(left[i])
            i += 1
        else:
            merged.append(right[j])
            j += 1

    while i < len(left):
        merged.append(left[i])
        i += 1

    while j < len(right):
        merged.append(right[j])
        j += 1

    return merged