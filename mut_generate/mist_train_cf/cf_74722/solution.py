"""
QUESTION:
Develop a function named `calculate_modulus` that takes two integers `num1` and `num2` as input and returns their modulus (remainder when `num1` is divided by `num2`). The result should be an integer.
"""

def calculate_modulus(num1, num2):
    '''
    This function returns the modulus of two numbers.
    :param num1: int: The first number.
    :param num2: int: The second number.
    :return: int: The modulus of num1 and num2.
    '''
    return num1 % num2