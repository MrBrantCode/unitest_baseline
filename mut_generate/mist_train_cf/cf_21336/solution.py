"""
QUESTION:
Write a function named `sum_array` that takes an array of integers as input and returns the sum of all integers in the array. The array may contain up to 10^9 integers.
"""

def sum_array(arr):
    """
    This function calculates the sum of all integers in a given array.

    Args:
        arr (list): A list of integers.

    Returns:
        int: The sum of all integers in the array.
    """
    return sum(arr)