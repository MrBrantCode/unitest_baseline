"""
QUESTION:
Create a function named "calculate_square_area" that takes the side length of a square as input and returns its area. The function should not accept any other arguments. The area should be calculated as the square of the side length.
"""

def calculate_square_area(side_length):
    """
    Calculate the area of a square given its side length.

    Args:
        side_length (float): The length of the side of the square.

    Returns:
        float: The area of the square.
    """
    return side_length ** 2