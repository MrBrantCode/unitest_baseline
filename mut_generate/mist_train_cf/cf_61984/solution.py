"""
QUESTION:
Write a function named `parallelogram_area` to calculate the surface area of a parallelogram. The function should take the base and height of the parallelogram as parameters and return the surface area.
"""

def parallelogram_area(base, height):
    """
    Calculate the surface area of a parallelogram.

    Args:
        base (float): The base of the parallelogram.
        height (float): The height of the parallelogram.

    Returns:
        float: The surface area of the parallelogram.
    """
    return base * height