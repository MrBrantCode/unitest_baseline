"""
QUESTION:
Create a function `is_real_number_in_range` that checks if a given real number is within the range of -1000 to 1000, inclusive. The function should return True if the number is within the specified range, and False otherwise. The input is a real number, and the output is a boolean value.
"""

def is_real_number_in_range(value):
    return value >= -1000 and value <= 1000