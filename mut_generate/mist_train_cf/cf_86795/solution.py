"""
QUESTION:
Write a function `reverse_string` that takes a string of lowercase English letters as input and returns the reversed string. The function must be implemented in a single line of code and cannot use any loops or string manipulation methods.
"""

def reverse_string(s):
    """
    Reverses a string of lowercase English letters.

    Args:
    s (str): The input string.

    Returns:
    str: The reversed string.
    """
    return s[::-1]