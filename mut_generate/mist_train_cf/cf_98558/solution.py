"""
QUESTION:
Create a function `verify_addition` that takes two numbers as input and calculates the difference between their sum and 2. The function should be able to handle a variety of inputs, including positive and negative integers.
"""

def verify_addition(a, b):
    """
    Calculate the difference between the sum of two numbers and 2.
    
    Parameters:
    a (int): The first number.
    b (int): The second number.
    
    Returns:
    int: The difference between the sum of a and b, and 2.
    """
    return a + b - 2