"""
QUESTION:
Create a Python function named "calculate_rectangle_area" that calculates and returns the area of a rectangle. The function should take two parameters: "length" and "width", both of which are variables that will hold the dimensions of the rectangle.
"""

def calculate_rectangle_area(length, width):
    """
    Calculate and return the area of a rectangle.

    Parameters:
    length (float): The length of the rectangle.
    width (float): The width of the rectangle.

    Returns:
    float: The area of the rectangle.
    """
    area = length * width
    return area