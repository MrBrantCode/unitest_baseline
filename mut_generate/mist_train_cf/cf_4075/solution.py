"""
QUESTION:
Write a function `multiply_add_square_divide` that takes an integer `x` as input, performs the following operations in order: multiplies `x` by 2, adds 5 to the result, squares the new result, and then divides it by 3, and returns the final result.
"""

def multiply_add_square_divide(x):
    return ((x * 2) + 5)**2 / 3