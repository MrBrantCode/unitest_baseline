"""
QUESTION:
Write a function `sum_two_numbers` that takes two integers `num1` and `num2` as input, calculates their sum, and prints it. The function should also return the calculated sum.
"""

def sum_two_numbers(num1, num2):
    total = num1 + num2
    print("The sum of", num1, "and", num2, "is:", total)
    return total