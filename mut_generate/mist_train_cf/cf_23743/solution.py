"""
QUESTION:
Write a function called `triangle_area` that calculates the area of a triangle given its base and height. The function should have two parameters: `base` and `height`, and it should return the calculated area.
"""

def triangle_area(base, height):
    """
    Calculate the area of a triangle given its base and height.

    Parameters:
    base (float): The base of the triangle.
    height (float): The height of the triangle.

    Returns:
    float: The calculated area of the triangle.
    """
    return 0.5 * base * height