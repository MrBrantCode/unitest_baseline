"""
QUESTION:
Write a Python function `fahrenheit_to_celsius` that takes a single argument `fahrenheit`, representing a temperature in Fahrenheit. The function should return the equivalent temperature in Celsius, calculated using the formula `(fahrenheit - 32) * 5.0/9.0`. The function should not take any additional arguments or perform any input validation.
"""

def fahrenheit_to_celsius(fahrenheit):
    return (fahrenheit - 32) * 5.0/9.0