"""
QUESTION:
Write a function `is_prime(n)` that checks whether a number `n` is prime or not and another function that checks if there is a prime number present in a given list of integers.
"""

import math

def is_prime(n):
    if n <= 1:
        return False
    if n <= 3:
        return True
    if n % 2 == 0 or n % 3 == 0:
        return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0:
            return False
        i += 6
    return True