"""
QUESTION:
Create a function `cubic_product` that takes three separate variables `a`, `b`, and `c` as input, calculates the cube of each variable, and returns the product of these cubed values. The function should work with any numeric inputs for `a`, `b`, and `c`.
"""

def cubic_product(a, b, c):
  return (a**3) * (b**3) * (c**3)