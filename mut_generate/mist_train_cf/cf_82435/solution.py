"""
QUESTION:
Create a function named `Kelvin_to_Fahrenheit` that takes a temperature in Kelvin as an argument and returns the equivalent temperature in Fahrenheit. The conversion formula is (K - 273.15) * 9/5 + 32, where K is the temperature in Kelvin.
"""

def Kelvin_to_Fahrenheit(kelvin):
    return (kelvin - 273.15) * 9/5 + 32