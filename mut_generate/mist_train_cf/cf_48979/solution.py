"""
QUESTION:
Write a function named `reverse_string` that takes a string `input_string` as input and returns the reversed string. The function should use slicing to achieve this.
"""

def reverse_string(input_string):
    """This function returns the reversed string.
  
    Args:
    input_string (str): the string to be reversed
  
    Returns:
    str: the reversed string
    """
    return input_string[::-1]