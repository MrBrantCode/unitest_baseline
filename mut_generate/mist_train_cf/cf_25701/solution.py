"""
QUESTION:
Write a function named `reverse_string` that takes a string as input and returns the reversed string. The function should use string slicing.
"""

def reverse_string(s):
    """
    This function takes a string as input and returns the reversed string.
    
    Parameters:
    s (str): The string to be reversed.
    
    Returns:
    str: The reversed string.
    """
    return s[::-1]