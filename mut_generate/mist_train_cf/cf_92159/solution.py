"""
QUESTION:
Write a function called `find_max_element` that finds the maximum element in a given array of integers, which can include negative numbers. The function should return the maximum element.
"""

def find_max_element(arr):
    """
    This function finds the maximum element in a given array of integers.
    
    Args:
        arr (list): A list of integers.
    
    Returns:
        int: The maximum element in the array.
    """
    return max(arr)