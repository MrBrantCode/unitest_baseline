"""
QUESTION:
Create a function named `calculate_triangle_area` that takes the base and height of a regular triangle as parameters and returns the area of the triangle. The function should calculate the area using the formula: area = (base * height) / 2.
"""

def calculate_triangle_area(base, height):
    """
    Calculate the area of a triangle given its base and height.

    Args:
    base (float): The base of the triangle.
    height (float): The height of the triangle.

    Returns:
    float: The area of the triangle.
    """
    return (base * height) / 2