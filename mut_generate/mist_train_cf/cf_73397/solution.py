"""
QUESTION:
Write a function called `calculate_total_miles_hiked` that takes two parameters: `miles_hiked_each_day` and `number_of_days`. The function should return the total miles hiked by multiplying these two parameters. The function should not take any other parameters.
"""

def calculate_total_miles_hiked(miles_hiked_each_day, number_of_days):
    """
    Calculate the total miles hiked by multiplying miles hiked each day with the number of days.

    Args:
        miles_hiked_each_day (int): The number of miles hiked each day.
        number_of_days (int): The number of days.

    Returns:
        int: The total miles hiked.
    """
    return miles_hiked_each_day * number_of_days