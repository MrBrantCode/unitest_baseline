"""
QUESTION:
Write a function `sort_list` that sorts a given list of numbers from smallest to largest. The function should take a list of integers as input and return the sorted list.
"""

def sort_list(numbers):
    """
    Sorts a given list of numbers from smallest to largest.
    
    Args:
        numbers (list): A list of integers.
    
    Returns:
        list: The sorted list of integers.
    """
    numbers.sort()
    return numbers