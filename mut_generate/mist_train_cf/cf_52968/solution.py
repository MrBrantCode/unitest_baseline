"""
QUESTION:
Calculate the surface area of a rectangle given its length and width in centimeters. The surface area is calculated by multiplying the length by the width. Create a function named `calculate_surface_area` to perform this calculation. The function should take two arguments, `length` and `width`, both of which are integers representing the dimensions of the rectangle in centimeters.
"""

def calculate_surface_area(length, width):
    """
    Calculate the surface area of a rectangle given its length and width in centimeters.

    Args:
    length (int): The length of the rectangle in centimeters.
    width (int): The width of the rectangle in centimeters.

    Returns:
    int: The surface area of the rectangle in square centimeters.
    """
    return length * width