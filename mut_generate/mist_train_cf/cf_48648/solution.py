"""
QUESTION:
Create a function `celsius_to_fahrenheit(c)` that takes a temperature in degrees Celsius as input and returns the equivalent temperature in degrees Fahrenheit. The conversion should be based on the formula F = C * 9/5 + 32, where F is the temperature in degrees Fahrenheit and C is the temperature in degrees Celsius.
"""

def celsius_to_fahrenheit(c):
    return c * 9/5 + 32