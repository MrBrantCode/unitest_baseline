"""
QUESTION:
Identify the dependent and independent variables in the equation y = x^2 + 5, where the dependent variable is the variable being predicted or measured and the independent variable is the variable being manipulated or changed.
"""

def calculate_dependent_variable(x):
    """
    Calculate the dependent variable y based on the given independent variable x.
    
    The equation is y = x^2 + 5.
    
    Parameters:
    x (float): The independent variable.
    
    Returns:
    float: The calculated dependent variable y.
    """
    return x ** 2 + 5