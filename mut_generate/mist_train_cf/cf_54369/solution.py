"""
QUESTION:
Write a function called `find_smallest_number` to find the smallest number in a list of floating-point numbers. The function should take a list of floating-point numbers as input and return the smallest number in the list.
"""

def find_smallest_number(numbers):
    """
    This function finds the smallest number in a list of floating-point numbers.

    Args:
        numbers (list): A list of floating-point numbers.

    Returns:
        float: The smallest number in the list.
    """
    return min(numbers)