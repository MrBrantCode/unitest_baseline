"""
QUESTION:
Create a function called `sort_number_array` that sorts a given number array in ascending sequence. The function should take a list of numbers as input and return the sorted list. The function should not have any restrictions on the length of the input list.
"""

def sort_number_array(number_array):
    """
    Sorts a given number array in ascending sequence.

    Args:
        number_array (list): A list of numbers.

    Returns:
        list: The sorted list of numbers.
    """
    return sorted(number_array)