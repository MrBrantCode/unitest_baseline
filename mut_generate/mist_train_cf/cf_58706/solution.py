"""
QUESTION:
Create a function called `calculate_array_sum` that calculates the sum of all elements in a given array. The input array will contain only integers, and the function should return the sum.
"""

def calculate_array_sum(array):
    """
    Calculate the sum of all elements in a given array.
    
    Args:
    array (list): A list of integers.
    
    Returns:
    int: The sum of all elements in the array.
    """
    return sum(array)