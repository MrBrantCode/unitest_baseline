"""
QUESTION:
Create a function called `calculate_triangle_area` to compute the area of a triangle. The function should take two parameters: `base` and `height`, both in centimeters, and return the area in square centimeters. The formula for the area of a triangle is (base * height) / 2.
"""

def calculate_triangle_area(base, height):
    """
    Calculate the area of a triangle given its base and height.
    
    Parameters:
    base (float): The base of the triangle in centimeters.
    height (float): The height of the triangle in centimeters.
    
    Returns:
    float: The area of the triangle in square centimeters.
    """
    return (base * height) / 2