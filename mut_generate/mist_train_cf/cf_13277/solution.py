"""
QUESTION:
Create a function `celsius_to_kelvin` that takes a single argument `celsius` representing the temperature in Celsius and returns the equivalent temperature in Kelvin. The conversion formula is `K = C + 273.15`, where `K` is the temperature in Kelvin and `C` is the temperature in Celsius.
"""

def celsius_to_kelvin(celsius):
    kelvin = celsius + 273.15
    return kelvin