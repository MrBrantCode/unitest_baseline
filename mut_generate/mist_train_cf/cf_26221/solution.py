"""
QUESTION:
Create a function called `repeat_string` that takes a string and an integer `n` as input and returns the string repeated `n` times. The function should return a string and should be able to handle any valid input string and a non-negative integer `n`.
"""

def entance(string, n):
    """
    This function takes a string and an integer as input and returns the 
    string repeated n times.

    Parameters:
    string (string): The string that needs to be repeated.
    n (int): The number of times the string needs to be repeated.

    Returns:
    string: The repeated string
    """
    return string * n