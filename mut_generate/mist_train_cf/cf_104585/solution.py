"""
QUESTION:
Write a function named `fahrenheit_to_celsius` that takes a single argument `fahrenheit_temp` representing a temperature value in Fahrenheit. The function should return the equivalent temperature in Celsius.
"""

def fahrenheit_to_celsius(fahrenheit_temp):
    """
    This function converts a temperature in Fahrenheit to Celsius.

    Parameters:
    fahrenheit_temp (float): The temperature in Fahrenheit.

    Returns:
    float: The equivalent temperature in Celsius.
    """
    return (fahrenheit_temp - 32) * 5/9