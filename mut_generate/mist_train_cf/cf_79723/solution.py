"""
QUESTION:
Write a function called sum_numbers that calculates the sum of three given numbers and returns the result. The input numbers will be decimal numbers.
"""

def sum_numbers(a, b, c):
    """
    This function calculates the sum of three given decimal numbers.
    
    Parameters:
    a (float): The first decimal number.
    b (float): The second decimal number.
    c (float): The third decimal number.
    
    Returns:
    float: The sum of a, b, and c.
    """
    return a + b + c