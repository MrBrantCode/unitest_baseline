"""
QUESTION:
Write a function named `reverse_word` that takes a string as input and returns the input string with its characters reversed. The input string may contain any characters.
"""

def reverse_word(word):
    """
    This function takes a string as input and returns the input string with its characters reversed.

    Parameters:
    word (str): The input string.

    Returns:
    str: The input string with its characters reversed.
    """
    return word[::-1]