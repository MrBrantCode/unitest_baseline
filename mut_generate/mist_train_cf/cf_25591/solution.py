"""
QUESTION:
Create a function named sort_list() that takes a list of integers as input and returns the sorted list in ascending numerical order.
"""

def sort_list(input_list):
    """
    Sorts a list of integers in ascending numerical order.

    Args:
        input_list (list): A list of integers.

    Returns:
        list: The sorted list of integers.
    """
    return sorted(input_list)