"""
QUESTION:
Write a function `merge_sort(arr)` that takes an array of numbers as input and returns a new array containing the same numbers sorted in ascending order. The function should achieve this using a single loop and without relying on built-in sorting functions or methods. The time complexity of the algorithm should be O(n log n).
"""

def merge_sort(arr):
    if len(arr) <= 1:
        return arr
    
    mid = len(arr) // 2
    left = merge_sort(arr[:mid])
    right = merge_sort(arr[mid:])
    
    return merge(left, right)

def merge(left, right):
    merged = []
    i = 0
    j = 0
    
    while i < len(left) and j < len(right):
        if left[i] < right[j]:
            merged.append(left[i])
            i += 1
        else:
            merged.append(right[j])
            j += 1
    
    merged.extend(left[i:])
    merged.extend(right[j:])
    
    return merged