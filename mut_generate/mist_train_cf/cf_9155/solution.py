"""
QUESTION:
Create a function `power_of_two` that calculates 2 raised to the power of an integer `n`, where `n` is between 1 and 1000 (inclusive). The function should return the result of this calculation.
"""

def power_of_two(n):
    """
    Calculate 2 raised to the power of n.
    
    Args:
        n (int): The power to which 2 is raised. Must be between 1 and 1000 (inclusive).
    
    Returns:
        int: The result of 2 raised to the power of n.
    """
    return 2 ** n