"""
QUESTION:
Evaluate the expression 2x + 3y - 7z, where x, y, and z are integers within the range of -1000 to 1000 (inclusive), given x = 500, y = -200, and z = 0.
"""

def evaluate_expression(x, y, z):
    """
    Evaluates the expression 2x + 3y - 7z.

    Args:
        x (int): The value of x.
        y (int): The value of y.
        z (int): The value of z.

    Returns:
        int: The result of the expression.
    """
    return 2 * x + 3 * y - 7 * z