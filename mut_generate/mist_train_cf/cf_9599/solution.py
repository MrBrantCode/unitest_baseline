"""
QUESTION:
Create a function `calculate_average` that takes three numbers as input and returns their average. The function should accept decimal numbers and return the average as a decimal number.
"""

def calculate_average(num1, num2, num3):
    """
    This function calculates the average of three given numbers.

    Args:
        num1 (float): The first number.
        num2 (float): The second number.
        num3 (float): The third number.

    Returns:
        float: The average of the three numbers.
    """
    return (num1 + num2 + num3) / 3