"""
QUESTION:
Write a function named `calculate_profit` that takes four parameters: `total_revenue`, `cost_of_goods_sold`, `operating_expenses`, and `taxes`, and returns the total profit. The total profit is calculated as the total revenue minus the cost of goods sold, operating expenses, and taxes.
"""

def calculate_profit(total_revenue, cost_of_goods_sold, operating_expenses, taxes):
    return total_revenue - cost_of_goods_sold - operating_expenses - taxes