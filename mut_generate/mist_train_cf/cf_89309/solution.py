"""
QUESTION:
Implement a function named `merge_sort` that takes an array of integers as input and returns a sorted array in ascending order. The array can contain at least 10 million elements and may have duplicate numbers. Do not use any built-in sorting functions or libraries. Ensure the implementation has a time complexity of O(n log n) and a space complexity of O(n).
"""

def merge_sort(arr):
    if len(arr) <= 1:
        return arr

    mid = len(arr) // 2
    left_half = arr[:mid]
    right_half = arr[mid:]

    left_half = merge_sort(left_half)
    right_half = merge_sort(right_half)

    return merge(left_half, right_half)


def merge(left, right):
    merged = []
    left_index = right_index = 0

    while left_index < len(left) and right_index < len(right):
        if left[left_index] <= right[right_index]:
            merged.append(left[left_index])
            left_index += 1
        else:
            merged.append(right[right_index])
            right_index += 1

    merged += left[left_index:]
    merged += right[right_index:]

    return merged