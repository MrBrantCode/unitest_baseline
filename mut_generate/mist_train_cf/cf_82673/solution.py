"""
QUESTION:
Write a function named `cube_number` that takes a numerical value as input and returns its cube. The function should work for any given numerical value and should not use built-in cubing functions, instead, it should use multiplication to calculate the cube.
"""

def cube_number(x):
    """
    This function calculates the cube of a given numerical value.

    Args:
        x (float): The input number to be cubed.

    Returns:
        float: The cube of the input number.
    """
    return x * x * x