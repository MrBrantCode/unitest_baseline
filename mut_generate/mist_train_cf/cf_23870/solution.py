"""
QUESTION:
Write a function `sort_strings` that sorts an array of strings in alphabetical order. The function should take one argument, an array of strings, and return the sorted array.
"""

def sort_strings(arr):
    """
    Sorts an array of strings in alphabetical order.

    Args:
        arr (list): A list of strings.

    Returns:
        list: The sorted list of strings.
    """
    return sorted(arr)