"""
QUESTION:
Create a function `surface_area(a, b, h)` that calculates the surface area of a right-angled triangular prism. The function should take the lengths of two sides of the right-angled triangle at the end of the prism (`a` and `b`) and the height of the prism (`h`) as input, and return the surface area of the prism.
"""

def surface_area(a, b, h):
    return a*b + b*h + a*h