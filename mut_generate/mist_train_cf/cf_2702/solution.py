"""
QUESTION:
Write a Python function named 'calculate_final_value' that takes an integer as input and performs the following operations: 
- multiplies the number by 3, 
- adds 2 to the result, and 
- divides the sum by 4 (integer division), 
and returns the final result.
"""

def calculate_final_value(a):
    return (a * 3 + 2) // 4