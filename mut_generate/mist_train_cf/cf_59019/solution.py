"""
QUESTION:
Write a function `get_larger_value` that takes two integers `x` and `y` as input and returns the larger of the two values. The function should be as computationally efficient as possible.
"""

def get_larger_value(x, y):
    """
    This function takes two integers as input and returns the larger of the two values.

    Args:
        x (int): The first integer.
        y (int): The second integer.

    Returns:
        int: The larger of the two input integers.
    """
    return max(x, y)