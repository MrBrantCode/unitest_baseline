"""
QUESTION:
Write a function `count_iterations(n: int, m: int, p: int) -> int` to calculate the total number of iterations for three nested loops with ranges `n`, `m`, and `p`, given the constraints: 1 <= n, m, p <= 10.
"""

def count_iterations(n: int, m: int, p: int) -> int:
    return n * m * p