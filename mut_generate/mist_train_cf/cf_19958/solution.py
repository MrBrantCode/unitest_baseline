"""
QUESTION:
Implement a function named `merge_sort` that sorts a given array in ascending order. The function should have a time complexity of O(n log n) and use a constant amount of additional memory. The input array is not guaranteed to be sorted and may contain duplicate elements. The function should be able to handle arrays of varying sizes, including empty arrays and arrays with a single element.
"""

def merge_sort(arr):
    if len(arr) <= 1:
        return arr

    mid = len(arr) // 2
    left = arr[:mid]
    right = arr[mid:]

    left = merge_sort(left)
    right = merge_sort(right)

    return merge(left, right)

def merge(left, right):
    merged = []
    i = j = 0

    while i < len(left) and j < len(right):
        if left[i] < right[j]:
            merged.append(left[i])
            i += 1
        else:
            merged.append(right[j])
            j += 1

    while i < len(left):
        merged.append(left[i])
        i += 1

    while j < len(right):
        merged.append(right[j])
        j += 1

    return merged