"""
QUESTION:
Implement a function called `merge_sort` that sorts an array of integers in non-decreasing order using the Merge Sort algorithm without using any built-in sorting functions, handling duplicate elements in the array, and optimizing for time complexity.
"""

def merge_sort(arr):
    def merge(left, right):
        result = []
        i = j = 0
        while i < len(left) and j < len(right):
            if left[i] <= right[j]:
                result.append(left[i])
                i += 1
            else:
                result.append(right[j])
                j += 1
        result.extend(left[i:])
        result.extend(right[j:])
        return result

    if len(arr) <= 1:
        return arr
    mid = len(arr) // 2
    left = merge_sort(arr[:mid])
    right = merge_sort(arr[mid:])
    return merge(left, right)