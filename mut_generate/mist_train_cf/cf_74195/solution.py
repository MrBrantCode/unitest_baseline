"""
QUESTION:
Create a function `quantum_superposition` that takes two complex numbers representing quantum states as input and returns their superposed state.
"""

def quantum_superposition(state1, state2):
    """
    This function takes two complex numbers representing quantum states as input and returns their superposed state.
    
    Parameters:
    state1 (complex): The first quantum state.
    state2 (complex): The second quantum state.
    
    Returns:
    complex: The superposed state.
    """
    return state1 + state2