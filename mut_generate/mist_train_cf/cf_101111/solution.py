"""
QUESTION:
Create a function called `sum` that takes two parameters `a` and `b`, and returns their sum. Then, create a class called `Adder` with an `add` method that takes two parameters `a` and `b` and returns their sum. Lastly, create a class called `Multiplier` with a `multiply` method that takes two parameters `a` and `b` and returns their product.
"""

def add(a, b):
    return a + b

class Adder:
    def add(self, a, b):
        return a + b

class Multiplier:
    def multiply(self, a, b):
        return a * b