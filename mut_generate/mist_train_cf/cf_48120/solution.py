def multiplication(a, b):
    """
    This function takes two parameters: a number and a multiplier, 
    and returns the product of these two inputs.

    Args:
        a (int or float): The number to be multiplied.
        b (int or float): The multiplier.

    Returns:
        int or float: The product of a and b.
    """
    return a * b