"""
QUESTION:
Create a function that takes an integer as input and returns the result of multiplying the input by 3. The function should not take any arguments other than the input integer and should return the result as an integer.
"""

def multiply_by_three(n):
    """
    Returns the result of multiplying the input integer by 3.

    Args:
        n (int): The input integer.

    Returns:
        int: The result of multiplying the input integer by 3.
    """
    return n * 3